void IJ_VU_HE(double v, double u, unsigned int& i, unsigned int& j, double& dx1, double& dx2) throw()
{
    double x1t, x1f, x2f;

    static const double x1_sub_RS_0=-4.8470981368299;
    static const double x1_sub_RS_1=-3.0050356196006;
    static const double ZS_1=-2.966611541626;
    static const double dist_x1_inv_0=53.744104271177;
    static const double dist_x1_inv_1=17.169898106832;
    static const double x2_sub_RS_0=775.66140954774;
    static const double dist_x2_inv_0=5.9825855652536e-002;
//
// transformations
    x1t=log(v);
//
    if(x1t>ZS_1) {
        x1f=(x1t-ZS_1)*dist_x1_inv_1;
        i=IROUND(x1f)+100;
        if(i>298) i=298;
    } else if(x1t<x1_sub_RS_1) {
        x1f=(x1t-x1_sub_RS_0)*dist_x1_inv_0;
        if(x1f>0.) i=IROUND(x1f);
        else i=0;
    } else {
        i=99;
    }
//
    x2f=(u-x2_sub_RS_0)*dist_x2_inv_0;
    if(x2f>0.) {
        j=IROUND(x2f);
        if(j>199) j=199;
    } else j=0;
//
    dx1=x1t-x1_VUHE[i];
    dx2=u-x2_VUHE[j];
}
